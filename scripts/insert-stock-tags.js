async function main() {
  // Batch insert using a single transaction-like approach
  const entries = Object.entries(tags);
  const batchSize = 20;
  let inserted = 0;

  for (const [ticker, tagList] of entries) {
    await db.execute({
      sql: 'INSERT OR REPLACE INTO stock_tags (ticker, tags, updated_at) VALUES (?, ?, datetime("now"))',
      args: [ticker, JSON.stringify(tagList)]
    });
    inserted++;
    if (inserted % 25 === 0) console.log(`Inserted ${inserted}/${entries.length}`);
  }

  const count = await db.execute('SELECT COUNT(*) as c FROM stock_tags');
  console.log('Total stock_tags rows:', count.rows[0].c);

  const samples = await db.execute({
    sql: 'SELECT ticker, tags FROM stock_tags WHERE ticker IN ("NVDA", "0700.HK", "0020.HK", "0288.HK", "0606.HK")',
    args: []
  });
  samples.rows.forEach(r => console.log(r.ticker, '->', r.tags));

  console.log('Done!');
}

