#!/usr/bin/env node
/**
 * Research Job Processor
 * Polls Claudius HQ for pending research jobs and generates Sun Tzu reports.
 *
 * Usage:
 *   npx tsx scripts/process-research-jobs.ts          # Process one job
 *   npx tsx scripts/process-research-jobs.ts --loop   # Run continuously
 *   npx tsx scripts/process-research-jobs.ts --dry-run # Show what would be done
 */

import { execSync } from "child_process";

const HQ_API = process.env.HQ_API_URL || "https://claudiusinc.com/api";
const HQ_API_KEY = process.env.HQ_API_KEY;

if (!HQ_API_KEY) {
  console.error("Error: HQ_API_KEY environment variable is required");
  process.exit(1);
}

const headers = {
  Authorization: `Bearer ${HQ_API_KEY}`,
  "Content-Type": "application/json",
};

async function fetchPendingJobs(): Promise<any[]> {
  const res = await fetch(`${HQ_API}/stocks/research?status=pending`, { headers });
  if (!res.ok) {
    throw new Error(`Failed to fetch jobs: ${res.status} ${await res.text()}`);
  }
  const data = await res.json();
  return data.jobs || [];
}

async function updateJobStatus(
  jobId: string,
  status: string,
  progress?: number,
  reportId?: number,
  errorMessage?: string
) {
  const body: any = { status };
  if (progress !== undefined) body.progress = progress;
  if (reportId !== undefined) body.report_id = reportId;
  if (errorMessage !== undefined) body.error_message = errorMessage;

  const res = await fetch(`${HQ_API}/stocks/research/${jobId}`, {
    method: "PATCH",
    headers,
    body: JSON.stringify(body),
  });

  if (!res.ok) {
    throw new Error(`Failed to update job ${jobId}: ${res.status} ${await res.text()}`);
  }
  return await res.json();
}

async function createReport(
  ticker: string,
  title: string,
  content: string,
  reportType: string = "sun-tzu"
) {
  const res = await fetch(`${HQ_API}/stocks/reports`, {
    method: "POST",
    headers,
    body: JSON.stringify({
      ticker,
      title,
      content,
      report_type: reportType,
    }),
  });

  if (!res.ok) {
    throw new Error(`Failed to create report: ${res.status} ${await res.text()}`);
  }
  const data = await res.json();
  return data.report;
}

