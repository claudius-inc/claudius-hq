async function processJob(job: any, dryRun: boolean = false): Promise<boolean> {
  const { id: jobId, ticker } = job;

  console.log(`\n━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━`);
  console.log(`Processing job: ${jobId} (${ticker})`);
  console.log(`━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━`);

  if (dryRun) {
    console.log(`[DRY RUN] Would update job to "processing"`);
    console.log(`[DRY RUN] Would generate Sun Tzu report for ${ticker}`);
    console.log(`[DRY RUN] Would create report via API`);
    console.log(`[DRY RUN] Would update job to "complete" with report_id`);
    return true;
  }

  try {
    // 1. Mark as processing
    console.log(`[${ticker}] Updating status to "processing"...`);
    await updateJobStatus(jobId, "processing", 10);

    // 2. Generate the report
    console.log(`[${ticker}] Generating report...`);
    const content = generateReportViaSkill(ticker);
    const title = `${ticker}: Sun Tzu Deep Dive`;

    // Update progress
    await updateJobStatus(jobId, "processing", 80);

    // 3. Create the report in HQ
    console.log(`[${ticker}] Saving report to HQ...`);
    const report = await createReport(ticker, title, content);
    console.log(`[${ticker}] Report created: ID ${report.id}`);

    // 4. Mark job as complete
    console.log(`[${ticker}] Marking job complete...`);
    await updateJobStatus(jobId, "complete", 100, report.id);

    console.log(`✅ Job ${jobId} completed successfully`);
    return true;
  } catch (error) {
    console.error(`❌ Job ${jobId} failed:`, error);
    try {
      await updateJobStatus(jobId, "failed", 0, undefined, String(error));
    } catch (e) {
      console.error(`Failed to mark job as failed:`, e);
    }
    return false;
  }
}

async function main() {
  const args = process.argv.slice(2);
  const dryRun = args.includes("--dry-run");
  const loop = args.includes("--loop");

  console.log("🔬 Research Job Processor");
  console.log(`Mode: ${dryRun ? "DRY RUN" : "LIVE"}`);
  console.log(`Loop: ${loop ? "YES" : "NO (single run)"}`);
  console.log(`API: ${HQ_API}`);
  console.log("");

  do {
    try {
      const jobs = await fetchPendingJobs();
      console.log(`Found ${jobs.length} pending job(s)`);

      if (jobs.length === 0) {
        console.log("No pending jobs. Exiting.");
        if (!loop) break;
        console.log("Waiting 30 seconds before next poll...");
        await new Promise((r) => setTimeout(r, 30000));
        continue;
      }

      // Process the oldest pending job first
      const job = jobs[jobs.length - 1];
      await processJob(job, dryRun);

      if (loop) {
        console.log("\nWaiting 5 seconds before next job...");
        await new Promise((r) => setTimeout(r, 5000));
      }
    } catch (error) {
      console.error("Fatal error:", error);
      if (!loop) process.exit(1);
      console.log("Retrying in 60 seconds...");
      await new Promise((r) => setTimeout(r, 60000));
    }
  } while (loop);
}

main();
